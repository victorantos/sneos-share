<script>
export default {
  name: 'AutogeneratedIndex',
  data() {
    return {
      isDarkMode: false,
      expandedCategories: {}
    };
  },
  computed: {
    currentYear() {
      return new Date().getFullYear();
    },
    totalPosts() {
      // This would be dynamically calculated based on the actual content
      return this.$page.frontmatter.totalPosts || 120;
    },
    totalCategories() {
      return this.$page.frontmatter.totalCategories || 8;
    },
    lastUpdated() {
      return this.$page.frontmatter.lastUpdated || new Date().toLocaleDateString();
    },
    categories() {
      // Get all pages and organize by category
      const pages = (this.$site?.pages || []).filter(page =>
        page.path.includes('/autogenerated/') &&
        page.path !== '/autogenerated/' &&
        !page.path.includes('/autogenerated/index')
      );

      const categoryMap = {};

      // Dynamically extract categories from paths
      pages.forEach(page => {
        const pathParts = page.path.split('/').filter(p => p);
        // Path structure: autogenerated/category/subcategory/...
        if (pathParts.length >= 2 && pathParts[0] === 'autogenerated') {
          const categorySlug = pathParts[1];

          // Create category if it doesn't exist
          if (!categoryMap[categorySlug]) {
            // Convert slug to readable name
            const categoryName = categorySlug
              .split('-')
              .map(word => word.charAt(0).toUpperCase() + word.slice(1))
              .join(' ');

            // Assign icons based on category name
            let icon = 'üìù';
            if (categoryName.toLowerCase().includes('finance')) icon = 'üí∞';
            else if (categoryName.toLowerCase().includes('law')) icon = '‚öñÔ∏è';
            else if (categoryName.toLowerCase().includes('education')) icon = 'üéì';
            else if (categoryName.toLowerCase().includes('tech')) icon = 'üíª';
            else if (categoryName.toLowerCase().includes('ai')) icon = 'ü§ñ';
            else if (categoryName.toLowerCase().includes('health')) icon = 'üè•';
            else if (categoryName.toLowerCase().includes('science')) icon = 'üî¨';
            else if (categoryName.toLowerCase().includes('art')) icon = 'üé®';
            else if (categoryName.toLowerCase().includes('design')) icon = '‚ú®';
            else if (categoryName.toLowerCase().includes('business')) icon = 'üíº';
            else if (categoryName.toLowerCase().includes('marketing')) icon = 'üìà';
            else if (categoryName.toLowerCase().includes('environment')) icon = 'üå±';
            else if (categoryName.toLowerCase().includes('food')) icon = 'üç≥';
            else if (categoryName.toLowerCase().includes('travel')) icon = '‚úàÔ∏è';
            else if (categoryName.toLowerCase().includes('sport') || categoryName.toLowerCase().includes('fitness')) icon = '‚öΩ';
            else if (categoryName.toLowerCase().includes('fashion')) icon = 'üëó';
            else if (categoryName.toLowerCase().includes('crypto')) icon = '‚Çø';
            else if (categoryName.toLowerCase().includes('security')) icon = 'üîí';
            else if (categoryName.toLowerCase().includes('data')) icon = 'üìä';
            else if (categoryName.toLowerCase().includes('automotive')) icon = 'üöó';
            else if (categoryName.toLowerCase().includes('real estate')) icon = 'üè†';
            else if (categoryName.toLowerCase().includes('gaming')) icon = 'üéÆ';
            else if (categoryName.toLowerCase().includes('music')) icon = 'üéµ';
            else if (categoryName.toLowerCase().includes('pet')) icon = 'üêæ';
            else if (categoryName.toLowerCase().includes('parent')) icon = 'üë∂';

            categoryMap[categorySlug] = {
              id: categorySlug,
              name: categoryName,
              icon: icon,
              items: []
            };
          }

          // Add page to category
          categoryMap[categorySlug].items.push({
            title: page.title,
            path: page.path
          });
        }
      });

      // Return categories sorted by item count
      return Object.values(categoryMap)
        .filter(cat => cat.items.length > 0)
        .sort((a, b) => b.items.length - a.items.length);
    }
  },
  mounted() {
    // Check for saved dark mode preference or system preference
    const savedTheme = localStorage.getItem('darkMode');
    if (savedTheme) {
      this.isDarkMode = savedTheme === 'true';
    } else {
      this.isDarkMode = window.matchMedia('(prefers-color-scheme: dark)').matches;
    }
    this.applyTheme();

    // Listen for system theme changes
    window.matchMedia('(prefers-color-scheme: dark)').addEventListener('change', (e) => {
      if (!localStorage.getItem('darkMode')) {
        this.isDarkMode = e.matches;
        this.applyTheme();
      }
    });
  },
  methods: {
    toggleCategory(categoryId) {
      this.expandedCategories[categoryId] = !this.expandedCategories[categoryId];
      this.$forceUpdate(); // Force update to reflect changes
    },
    scrollToCategory(categoryId) {
      const element = document.getElementById(categoryId);
      if (element) {
        element.scrollIntoView({ behavior: 'smooth' });
      }
    },
    toggleDarkMode() {
      this.isDarkMode = !this.isDarkMode;
      localStorage.setItem('darkMode', this.isDarkMode.toString());
      this.applyTheme();
    },
    applyTheme() {
      if (this.isDarkMode) {
        document.documentElement.classList.add('dark');
      } else {
        document.documentElement.classList.remove('dark');
      }
    }
  }
};
</script>

