                <span class="category-name">{{ category.name }}</span>
                <span class="category-count">({{ getCategoryTotal(category) }})</span>
                <span class="category-toggle">{{ expandedCategories[category.id] ? '‚ñº' : '‚ñ∂' }}</span>
              </button>
