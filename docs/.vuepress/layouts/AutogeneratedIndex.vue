<script>
export default {
  name: 'AutogeneratedIndex',
  data() {
    return {
      isDarkMode: false,
      expandedCategories: {},
      expandedSubcategories: {}
    };
  },
  computed: {
    currentYear() {
      return new Date().getFullYear();
    },
    totalPosts() {
      if (this.$page.frontmatter.totalPosts) return this.$page.frontmatter.totalPosts;
      return this.categories.reduce((sum, cat) => {
        const directItems = cat.items?.length || 0;
        const subcategoryItems = cat.subcategories?.reduce((subSum, sub) => subSum + (sub.items?.length || 0), 0) || 0;
        return sum + directItems + subcategoryItems;
      }, 0);
    },
    totalCategories() {
      return this.$page.frontmatter.totalCategories || this.categories.length;
    },
    lastUpdated() {
      return this.$page.frontmatter.lastUpdatedDate || new Date().toLocaleDateString();
    },
    categories() {
      // Get all pages in ai-library directory
      const pages = (this.$site?.pages || []).filter(page =>
        page.path.startsWith('/ai-library/') &&
        page.path !== '/ai-library/' &&
        !page.path.endsWith('/ai-library/index.html')
      );

      const categoryMap = {};

      // Organize pages by category
      pages.forEach(page => {
        const pathParts = page.path.replace(/^\/ai-library\//, '').split('/').filter(p => p);

        if (pathParts.length >= 1) {
          const categorySlug = pathParts[0];

          if (!categoryMap[categorySlug]) {
            const categoryName = categorySlug
              .split('-')
              .map(word => word.charAt(0).toUpperCase() + word.slice(1))
              .join(' ');

            let icon = 'üìù';
            const lowerName = categoryName.toLowerCase();
            if (lowerName.includes('finance') || lowerName.includes('investment')) icon = 'üí∞';
            else if (lowerName.includes('law') || lowerName.includes('legal')) icon = '‚öñÔ∏è';
            else if (lowerName.includes('education') || lowerName.includes('learning')) icon = 'üéì';
            else if (lowerName.includes('tech') || lowerName.includes('programming')) icon = 'üíª';
            else if (lowerName.includes('ai') || lowerName.includes('comparison')) icon = 'ü§ñ';
            else if (lowerName.includes('health') || lowerName.includes('medical') || lowerName.includes('wellness')) icon = 'üè•';
            else if (lowerName.includes('science') || lowerName.includes('research')) icon = 'üî¨';
            else if (lowerName.includes('art') || lowerName.includes('creative')) icon = 'üé®';
            else if (lowerName.includes('design')) icon = '‚ú®';
            else if (lowerName.includes('business') || lowerName.includes('entrepreneur')) icon = 'üíº';
            else if (lowerName.includes('marketing') || lowerName.includes('seo')) icon = 'üìà';
            else if (lowerName.includes('environment') || lowerName.includes('sustainability')) icon = 'üå±';
            else if (lowerName.includes('food') || lowerName.includes('cooking')) icon = 'üç≥';
            else if (lowerName.includes('travel')) icon = '‚úàÔ∏è';
            else if (lowerName.includes('sport') || lowerName.includes('fitness')) icon = '‚öΩ';
            else if (lowerName.includes('fashion') || lowerName.includes('beauty')) icon = 'üëó';
            else if (lowerName.includes('crypto') || lowerName.includes('blockchain')) icon = '‚Çø';
            else if (lowerName.includes('security') || lowerName.includes('privacy')) icon = 'üîí';
            else if (lowerName.includes('data') || lowerName.includes('analytics')) icon = 'üìä';
            else if (lowerName.includes('automotive') || lowerName.includes('vehicle')) icon = 'üöó';
            else if (lowerName.includes('real estate') || lowerName.includes('property')) icon = 'üè†';
            else if (lowerName.includes('gaming') || lowerName.includes('esport')) icon = 'üéÆ';
            else if (lowerName.includes('music')) icon = 'üéµ';
            else if (lowerName.includes('pet')) icon = 'üêæ';
            else if (lowerName.includes('parent') || lowerName.includes('child')) icon = 'üë∂';
            else if (lowerName.includes('home') || lowerName.includes('diy')) icon = 'üè°';
            else if (lowerName.includes('personal') || lowerName.includes('productivity')) icon = 'üìã';

            categoryMap[categorySlug] = {
              id: categorySlug,
              name: categoryName,
              icon: icon,
              items: [],
              subcategories: {}
            };
          }

          // Add subcategory if exists
          if (pathParts.length >= 2) {
            const subcategorySlug = pathParts[1];

            if (!categoryMap[categorySlug].subcategories[subcategorySlug]) {
              const subcategoryName = subcategorySlug
                .split('-')
                .map(word => word.charAt(0).toUpperCase() + word.slice(1))
                .join(' ');

              categoryMap[categorySlug].subcategories[subcategorySlug] = {
                id: subcategorySlug,
                name: subcategoryName,
                items: []
              };
            }

            categoryMap[categorySlug].subcategories[subcategorySlug].items.push({
              title: page.title || page.path.split('/').pop().replace('.html', ''),
              path: page.path
            });
          } else {
            categoryMap[categorySlug].items.push({
              title: page.title || page.path.split('/').pop().replace('.html', ''),
              path: page.path
            });
          }
        }
      });

      // Convert to array and sort
      return Object.values(categoryMap)
        .filter(cat => cat.items.length > 0 || Object.keys(cat.subcategories).length > 0)
        .sort((a, b) => {
          const aTotal = a.items.length + Object.values(a.subcategories).reduce((sum, sub) => sum + sub.items.length, 0);
          const bTotal = b.items.length + Object.values(b.subcategories).reduce((sum, sub) => sum + sub.items.length, 0);
          return bTotal - aTotal;
        })
        .map(cat => ({
          ...cat,
          subcategories: Object.values(cat.subcategories).sort((a, b) => b.items.length - a.items.length)
        }));
    }
  },
  mounted() {
    // Check for saved dark mode preference or system preference
    const savedTheme = localStorage.getItem('darkMode');
    if (savedTheme) {
      this.isDarkMode = savedTheme === 'true';
    } else {
      this.isDarkMode = window.matchMedia('(prefers-color-scheme: dark)').matches;
    }
    this.applyTheme();

    // Listen for system theme changes
    window.matchMedia('(prefers-color-scheme: dark)').addEventListener('change', (e) => {
      if (!localStorage.getItem('darkMode')) {
        this.isDarkMode = e.matches;
        this.applyTheme();
      }
    });

    // Debug: Log categories to console
    console.log('Categories loaded:', this.categories);
    console.log('Total pages:', this.$site?.pages?.length);
    console.log('AI Library pages:', this.$site?.pages?.filter(p => p.path.startsWith('/ai-library/')).length);
  },
  methods: {
    getCategoryTotal(category) {
      const directItems = category.items?.length || 0;
      const subcategoryItems = category.subcategories?.reduce((sum, sub) => sum + (sub.items?.length || 0), 0) || 0;
      return directItems + subcategoryItems;
    },
    toggleCategory(categoryId) {
      this.expandedCategories[categoryId] = !this.expandedCategories[categoryId];
      this.$forceUpdate();
    },
    toggleSubcategory(categoryId, subcategoryId) {
      const key = categoryId + '-' + subcategoryId;
      this.expandedSubcategories[key] = !this.expandedSubcategories[key];
      this.$forceUpdate();
    },
    scrollToCategory(categoryId) {
      const element = document.getElementById(categoryId);
      if (element) {
        element.scrollIntoView({ behavior: 'smooth' });
      }
    },
    toggleDarkMode() {
      this.isDarkMode = !this.isDarkMode;
      localStorage.setItem('darkMode', this.isDarkMode.toString());
      this.applyTheme();
    },
    applyTheme() {
      if (this.isDarkMode) {
        document.documentElement.classList.add('dark');
      } else {
        document.documentElement.classList.remove('dark');
      }
    }
  }
};
</script>

