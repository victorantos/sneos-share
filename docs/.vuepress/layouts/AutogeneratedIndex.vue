<script>
export default {
  name: 'AutogeneratedIndex',
  data() {
    return {
      isDarkMode: false,
      expandedCategories: {}
    };
  },
  computed: {
    currentYear() {
      return new Date().getFullYear();
    },
    totalPosts() {
      // This would be dynamically calculated based on the actual content
      return this.$page.frontmatter.totalPosts || 120;
    },
    totalCategories() {
      return this.$page.frontmatter.totalCategories || 8;
    },
    lastUpdated() {
      return this.$page.frontmatter.lastUpdated || new Date().toLocaleDateString();
    },
    categories() {
      // Get all pages and organize by category
      const pages = (this.$site?.pages || []).filter(page =>
        page.path.includes('/compare/') &&
        page.path !== '/compare/' &&
        !page.path.includes('/compare/autogenerated/index')
      );

      const categoryMap = {
        finance: { id: 'finance', name: 'Finance', icon: 'üí∞', items: [] },
        law: { id: 'law', name: 'Law', icon: '‚öñÔ∏è', items: [] },
        research: { id: 'research', name: 'Research', icon: 'üî¨', items: [] },
        education: { id: 'education', name: 'Education', icon: 'üéì', items: [] },
        technology: { id: 'technology', name: 'Technology', icon: 'üíª', items: [] },
        ai: { id: 'ai', name: 'AI & Future', icon: 'ü§ñ', items: [] },
        lifestyle: { id: 'lifestyle', name: 'Lifestyle', icon: 'üåü', items: [] },
        other: { id: 'other', name: 'Other', icon: 'üìù', items: [] }
      };

      // Categorize pages based on title/tags
      pages.forEach(page => {
        const title = page.title || '';
        const titleLower = title.toLowerCase();
        const item = { title: page.title, path: page.path };

        if (titleLower.includes('finance') || titleLower.includes('investment') ||
            titleLower.includes('stock') || titleLower.includes('retirement') ||
            titleLower.includes('tax') || titleLower.includes('credit')) {
          categoryMap.finance.items.push(item);
        } else if (titleLower.includes('law') || titleLower.includes('legal') ||
                   titleLower.includes('court') || titleLower.includes('lawyer')) {
          categoryMap.law.items.push(item);
        } else if (titleLower.includes('research') || titleLower.includes('medical') ||
                   titleLower.includes('science') || titleLower.includes('brain')) {
          categoryMap.research.items.push(item);
        } else if (titleLower.includes('education') || titleLower.includes('learning') ||
                   titleLower.includes('student') || titleLower.includes('university')) {
          categoryMap.education.items.push(item);
        } else if (titleLower.includes('technology') || titleLower.includes('tech') ||
                   titleLower.includes('quantum') || titleLower.includes('space')) {
          categoryMap.technology.items.push(item);
        } else if (titleLower.includes('ai') || titleLower.includes('artificial intelligence') ||
                   titleLower.includes('future')) {
          categoryMap.ai.items.push(item);
        } else if (titleLower.includes('life') || titleLower.includes('health') ||
                   titleLower.includes('relationship') || titleLower.includes('career')) {
          categoryMap.lifestyle.items.push(item);
        } else {
          categoryMap.other.items.push(item);
        }
      });

      // Return only categories with items, sorted by item count
      return Object.values(categoryMap)
        .filter(cat => cat.items.length > 0)
        .sort((a, b) => b.items.length - a.items.length);
    }
  },
  mounted() {
    // Check for saved dark mode preference or system preference
    const savedTheme = localStorage.getItem('darkMode');
    if (savedTheme) {
      this.isDarkMode = savedTheme === 'true';
    } else {
      this.isDarkMode = window.matchMedia('(prefers-color-scheme: dark)').matches;
    }
    this.applyTheme();

    // Listen for system theme changes
    window.matchMedia('(prefers-color-scheme: dark)').addEventListener('change', (e) => {
      if (!localStorage.getItem('darkMode')) {
        this.isDarkMode = e.matches;
        this.applyTheme();
      }
    });
  },
  methods: {
    toggleCategory(categoryId) {
      this.expandedCategories[categoryId] = !this.expandedCategories[categoryId];
      this.$forceUpdate(); // Force update to reflect changes
    },
    scrollToCategory(categoryId) {
      const element = document.getElementById(categoryId);
      if (element) {
        element.scrollIntoView({ behavior: 'smooth' });
      }
    },
    toggleDarkMode() {
      this.isDarkMode = !this.isDarkMode;
      localStorage.setItem('darkMode', this.isDarkMode.toString());
      this.applyTheme();
    },
    applyTheme() {
      if (this.isDarkMode) {
        document.documentElement.classList.add('dark');
      } else {
        document.documentElement.classList.remove('dark');
      }
    }
  }
};
</script>

